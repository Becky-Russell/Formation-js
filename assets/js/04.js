/*-------------------------
Les opérateurs arithmetiques
------------------------*/

// ###### L'ADDITION #######/

// Déclaration de plusieurs variables a la suite ..
var nb1, nb2, resultat;

nb1 = 11; nb2 = 5; 

// -- L'addition de nb1  + nb2 avec l'opérateur " + "

resultat = nb1 + nb2;
console.log(" L'Addition de nb1 et de nb2 est égale à : " + resultat);

// ###### LA SOUSTRACTION #######/

// -- La soustration de nb1 - nb2 avec l'opérateur " - "

resultat = nb1 - nb2;
console.log(" La soustraction de nb1 et de nb2 est égale à : " + resultat);

// ###### LA MULTIPLICATION #######/

// -- La multiplication de nb1  * nb2 avec l'opérateur " * "

resultat = nb1 * nb2;
console.log(" La multiplication de nb1 et de nb2 est égale à : " + resultat) ;

// ###### LA DIVISION #######/

// -- La division de nb1 / nb2 avec l'opérateur " / "

resultat = nb1 / nb2;
console.log(" La Division de nb1 et de nb2 est égale à : " + resultat) ;

// ###### LE MODULO #######/

// -- Le Modulo correspond au reste de la division.
// -- Le modulo de nb1  et nb2 avec l'opérateur " % "

resultat = nb1 % nb2;
console.log(" Le modulo de nb1 et de nb2 est égale à : " + resultat) ;

