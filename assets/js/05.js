/* ------------------------------
LES Incremantations et Décremantations
--------------------------------*/

// ###### Incrémentation #####/

var nb1 = 1;
nb1 = nb1 + 1;

// Ecriture simpllifiée
nb1++; // Raccourci de nb1 = nb1 + 1; Par pas de 1

// ############### Décremantation ############ //

nb1 = nb1 - 1;
// Ecriture simpllifiée
nb1--; // Raccourci de nb1 = nb1 - 1; Par pas de 1

// ############### Subtilités ############ //

nb1 = 0;
console.log(nb1++);
console.log(nb1);

nb1 = 0;
console.log(++nb1);



